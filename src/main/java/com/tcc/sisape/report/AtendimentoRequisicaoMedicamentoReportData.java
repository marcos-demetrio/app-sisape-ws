package com.tcc.sisape.report;

public class AtendimentoRequisicaoMedicamentoReportData {

	private String cidadaoNome;
	private String cidadaoCpf;
	private String profissionalNome;
	private String medicamentoNome;
	private String medicamentoPosologia;

	public String getCidadaoNome() {
		return cidadaoNome;
	}

	public void setCidadaoNome(String cidadaoNome) {
		this.cidadaoNome = cidadaoNome;
	}

	public String getCidadaoCpf() {
		return cidadaoCpf;
	}

	public void setCidadaoCpf(String cidadaoCpf) {
		this.cidadaoCpf = cidadaoCpf;
	}

	public String getProfissionalNome() {
		return profissionalNome;
	}

	public void setProfissionalNome(String profissionalNome) {
		this.profissionalNome = profissionalNome;
	}

	public String getMedicamentoNome() {
		return medicamentoNome;
	}

	public void setMedicamentoNome(String medicamentoNome) {
		this.medicamentoNome = medicamentoNome;
	}

	public String getMedicamentoPosologia() {
		return medicamentoPosologia;
	}

	public void setMedicamentoPosologia(String medicamentoPosologia) {
		this.medicamentoPosologia = medicamentoPosologia;
	}
}
